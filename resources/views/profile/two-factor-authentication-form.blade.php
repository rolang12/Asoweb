        </div>

        @if ($this->enabled)
            @if ($showingQrCode)
                <div class="mt-4 max-w-xl text-sm text-gray-600">
                    <p class="font-semibold">
                        @if ($showingConfirmation)
                            {{ __('Para finalizar la activación, escanea el código QR usando tu celular o ingresa la contraseña y proveela al código OTP generado.') }}
